
#include <iostream>

int main()
{

    // Print name
    std::cout << "Hello, My Name!" << std::endl;

    // Add another line
    std::cout << "Welcome to C++ programming!" << std::endl;

    // Replacing std::endl;
    std::cout << "Replacing std::endl with a newline character.\n";

    // Multiple cout statements
    std::cout << "Hello";
    std::cout << " ";
    std::cout << "World\n";


    return 0;
}






